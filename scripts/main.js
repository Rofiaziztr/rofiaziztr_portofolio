const initInteractivity = () => {
  // Magnetic Buttons & Links
  const magneticItems = document.querySelectorAll(
    ".cta-row a, .btn, .btn-icon-only, nav a",
  );
  magneticItems.forEach((item) => {
    item.addEventListener("mousemove", (e) => {
      const { left, top, width, height } = item.getBoundingClientRect();
      const x = (e.clientX - left - width / 2) * 0.35;
      const y = (e.clientY - top - height / 2) * 0.35;
      item.style.transform = `translate(${x}px, ${y}px) scale(1.08)`;
    });
    item.addEventListener("mouseleave", () => {
      item.style.transform = "";
    });
  });

  // Project Modals
  const modal = document.getElementById("project-modal");
  const modalBody = document.getElementById("modal-body");
  const closeBtn = document.querySelector(".modal-close");
  const cards = document.querySelectorAll(".project-card");

  cards.forEach((card) => {
    card.addEventListener("click", (e) => {
      // Don't open modal if clicking the direct image link
      if (e.target.closest(".project-card__media-link")) return;

      const content = card.innerHTML;
      modalBody.innerHTML = `<div class="modal-project-view">${content}</div>`;
      modal.classList.add("active");
      document.body.style.overflow = "hidden";
    });
  });

  const closeModal = () => {
    modal.classList.remove("active");
    document.body.style.overflow = "";
    setTimeout(() => {
      modalBody.innerHTML = "";
    }, 300);
  };

  closeBtn?.addEventListener("click", closeModal);
  modal?.addEventListener("click", (e) => {
    if (e.target === modal) closeModal();
  });

  // Heading reveal on scroll (extending the reveal logic)
  const headings = document.querySelectorAll(".section-heading");
  const hObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
        }
      });
    },
    { threshold: 0.2 },
  );
  headings.forEach((h) => hObserver.observe(h));
};

const initMobileMenu = () => {
  const menuToggle = document.querySelector(".menu-toggle");
  const nav = document.querySelector(".nav");
  const navLinks = document.querySelectorAll(".nav a");

  if (!menuToggle || !nav) return;

  const toggleMenu = () => {
    const isExpanded = menuToggle.getAttribute("aria-expanded") === "true";
    menuToggle.setAttribute("aria-expanded", !isExpanded);
    nav.classList.toggle("is-active");
    // Prevent scrolling when menu is open
    document.body.style.overflow = isExpanded ? "" : "hidden";
  };

  menuToggle.addEventListener("click", toggleMenu);

  // Close menu when a link is clicked
  navLinks.forEach((link) => {
    link.addEventListener("click", () => {
      menuToggle.setAttribute("aria-expanded", "false");
      nav.classList.remove("is-active");
      document.body.style.overflow = "";
    });
  });

  // Close menu if user clicks outside
  document.addEventListener("click", (e) => {
    if (
      nav.classList.contains("is-active") &&
      !nav.contains(e.target) &&
      !menuToggle.contains(e.target)
    ) {
      toggleMenu();
    }
  });
};

